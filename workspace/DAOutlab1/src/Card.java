
public class Card {
	private int cardNum;
	private String suit;
	//Constructor assigns the card number and suit
	public Card(int inNum,String inSuit){
		cardNum = inNum;
		suit = inSuit;
	}
	public String getSuit(){ //Returns the suit as a string
		return suit;
	}
	public int getNum(){ //Returns the card number 11=Jack 12=Queen 13=King 14 = Ace
		return cardNum;
	}
}
